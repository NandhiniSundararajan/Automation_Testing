public class SwapTwoNumbers {

    public static void main(String[] args) {
        double a = 300.00;
        double b = 500.50;
        double c;
        System.out.println("Values before swapping: a = " + a + ", b = " + b);
        c = a;
        a = b;
        b = c;
        System.out.println("Values after swapping: a = " + a + ", b = " + b);

    }
}
